
public class point {
	
	private int x , y;
	
	public point() {
		
		x=0;
		y=0;
		}
	
	public point (int x , int y) {
		this.x=x;
		this.y=y;
		
	} 
	
	public void setx(int x) {
		
		this.x=x;
		
	}

public int getx() {
		
		return x;
		
	}

public void sety(int y) {
	
	this.y=y;
	
}

public int gety() {
	
	return y;
	
}

}
